<?php
namespace App\Models;
class Regempresas{
   public static function insertar($data = []){
		if(!empty($data)){
			$sql = "INSERT INTO t_ref_empresas(ref_empresa) VALUES(:ref_empresa)";
			try{
				$con = \Core\Model::getInstance();
				$result = $con->consulta($sql, $data);
				if(!empty($result)){
					return $result;
				} else{
					$result = false;
				}
			}catch(\PDOException $e){
				return $e->getMessage();
			}
		}
	}

